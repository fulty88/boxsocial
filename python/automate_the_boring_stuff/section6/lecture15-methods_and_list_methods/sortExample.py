spam = [2, 5, 3.14, 1, -7]
print(spam)
print('Sorting my list of numbers...')
spam.sort()
print(spam)

spam = ['ants', 'cats', 'dogs', 'badgers', 'elephants']
print(spam)
print('Sort my alphabetical list')
spam.sort()
print(spam)

print('Reverse alphabetical order')
spam.sort(reverse=True)
print(spam)
